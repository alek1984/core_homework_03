from datetime import datetime

def get_days_from_today(date):
    """
    Розраховує кількість днів між заданою датою і поточною датою.

    Аргументи:
    date (str): Дата у форматі 'РРРР-ММ-ДД'.

    Повертає:
    int: Кількість днів між заданою датою та поточною датою.
         Повертає додатне число для минулих дат, від'ємне — для майбутніх.
    str: Повідомлення про помилку, якщо формат дати неправильний.
    """
    try:
        # Перетворюємо рядок у дату
        input_date = datetime.strptime(date, '%Y-%m-%d').date()
        # Отримуємо поточну дату
        today_date = datetime.today().date()
        # Розраховуємо різницю у днях
        delta = today_date - input_date
        return delta.days
    except ValueError:
        # Обробляємо неправильний формат дати
        return "Неправильний формат дати. Використовуйте формат 'РРРР-ММ-ДД'."

# Приклади використання
print(get_days_from_today("2021-10-09"))  # Для минулої дати, наприклад, повертає додатне значення
print(get_days_from_today("2030-01-01"))  # Для майбутньої дати повертає від'ємне значення
print(get_days_from_today("неправильна_дата"))  # Неправильний формат дати
 